export interface WorldDot {
  x: number;
  y: number;
}

export const MAP_W = 720;
export const MAP_H = 300;
const CELL = 10;

export const worldDots: WorldDot[] = (() => {
  const dots: WorldDot[] = [];
  WORLD_ASCII.forEach((row, r) => {
    for (let c = 0; c < row.length; c++) {
      if (row[c] === "#") {
        dots.push({ x: c * CELL + CELL / 2, y: r * CELL + CELL / 2 });
      }
    }
  });
  return dots;
})();

export function project(lon: number, lat: number): { x: number; y: number } {
  return {
    x: ((lon + 180) / 360) * MAP_W,
    y: ((80 - lat) / 150) * MAP_H,
  };
}

